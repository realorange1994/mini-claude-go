// generateGoLookupFile creates a Go source file with a lazy-load lookup table
// mapping Lisp function names to Go import paths. This avoids loading all
// wrappers at startup — instead, the eval loop auto-imports on first use.
func generateGoLookupFile(infos []PkgInfo) error {
	var b strings.Builder
	b.WriteString("// Code generated by microlisp/ffi_gen/main.go - DO NOT EDIT.\n")
	b.WriteString("// Lazy-load lookup table: Lisp name -> Go import path.\n")
	b.WriteString("// When a symbol is not found in the environment, the eval loop\n")
	b.WriteString("// checks this table and auto-imports the Go function on first use.\n\n")
	b.WriteString("package microlisp\n\n")

	totalEntries := 0
	totalSkipped := 0

	// Collect all entries - deduplicate Lisp names
	type entry struct {
		lispName string
		goPath   string
	}
	var entries []entry
	seenLispNames := map[string]bool{}

	for _, info := range infos {
		pkgSkipped := 0
		for _, fn := range info.Functions {
			sig := fn.Signature
			wrapper, skipReason := generateWrapperForFn(info.ImportPath, info.Name, sig)
			if skipReason != "" {
				pkgSkipped++
				continue
			}
			if wrapper == "" {
				continue
			}
			// Extract lisp name from the generated wrapper
			// Format: (define (lisp-name ...) ...)
			lispName := extractLispName(wrapper)
			if lispName == "" {
				continue
			}
			goPath := info.ImportPath + "." + sig.Name
			if seenLispNames[lispName] {
				// Duplicate Lisp name — disambiguate with package prefix
				parts := strings.SplitN(info.ImportPath, "/", 2)
				pkgSuffix := parts[len(parts)-1]
				lispName = strings.ReplaceAll(pkgSuffix, "/", "-") + "-" + lispName
				if seenLispNames[lispName] {
					pkgSkipped++
					continue
				}
			}
			seenLispNames[lispName] = true
			entries = append(entries, entry{lispName, goPath})
		}
		totalSkipped += pkgSkipped
	}

	// Sort by Lisp name for deterministic output
	sort.Slice(entries, func(i, j int) bool { return entries[i].lispName < entries[j].lispName })

	b.WriteString("// GoFFILazyTable maps Lisp function names to Go import paths.\n")
	b.WriteString("// When a symbol lookup fails, the evaluator checks this table\n")
	b.WriteString("// and auto-imports the Go function, then caches it in the environment.\n")
	b.WriteString("var GoFFILazyTable = map[string]string{\n")

	for _, e := range entries {
		b.WriteString(fmt.Sprintf("\t%q: %q,\n", e.lispName, e.goPath))
		totalEntries++
	}

	b.WriteString("}\n\n")

	// Also generate a reverse lookup for go:apropos / help
	b.WriteString("// GoFFILazyTableByPkg groups Lisp names by Go package for apropos/help.\n")
	b.WriteString("var GoFFILazyTableByPkg = map[string][]string{\n")
	byPkg := map[string][]string{}
	for _, e := range entries {
		parts := strings.SplitN(e.goPath, ".", 2)
		pkg := parts[0]
		byPkg[pkg] = append(byPkg[pkg], e.lispName)
	}
	pkgs := make([]string, 0, len(byPkg))
	for p := range byPkg {
		pkgs = append(pkgs, p)
	}
	sort.Strings(pkgs)
	for _, p := range pkgs {
		names := byPkg[p]
		sort.Strings(names)
		b.WriteString(fmt.Sprintf("\t%q: {\n", p))
		for _, n := range names {
			b.WriteString(fmt.Sprintf("\t\t%q,\n", n))
		}
		b.WriteString("\t},\n")
	}
	b.WriteString("}\n")

	log.Printf("Generated Go lazy-load table: %d entries (%d skipped)", totalEntries, totalSkipped)

	outPath := "go_auto_wrapper_table.go"
	return os.WriteFile(outPath, []byte(b.String()), 0644)
}

// extractLispName extracts the function name from a (define (name ...) ...) form.
func extractLispName(wrapper string) string {
	// Format: (define (lisp-name ...) ...)
	i := strings.Index(wrapper, "(define (")
	if i < 0 {
		return ""
	}
	rest := wrapper[i+len("(define ("):]
	j := strings.IndexByte(rest, ' ')
	k := strings.IndexByte(rest, ')')
	end := len(rest)
	if j > 0 && j < end {
		end = j
	}
	if k > 0 && k < end {
		end = k
	}
	return rest[:end]
}

// generateLispFile creates a .lisp file with (define) wrappers for all
// Go stdlib functions whose signatures are compatible with Lisp type system.
// NOTE: This is kept for reference/debugging but is NOT loaded at runtime.
// The Go lookup table (go_auto_wrapper_table.go) is used instead for lazy loading.
func generateLispFile(infos []PkgInfo) error {
	var b strings.Builder
	b.WriteString(";;; Auto-generated by microlisp/ffi_gen/main.go - DO NOT EDIT.\n")
	b.WriteString(";;; Lisp wrappers for Go stdlib functions.\n")
	b.WriteString(";;; Each wrapper hides Go type system behind (funcall (go:import ...)) pattern.\n\n")

	totalWrappers := 0
	totalSkipped := 0

	for _, info := range infos {
		pkgWrappers := []string{}
		pkgSkipped := 0

		for _, fn := range info.Functions {
			sig := fn.Signature
			wrapper, skipReason := generateWrapperForFn(info.ImportPath, info.Name, sig)
			if wrapper != "" {
				pkgWrappers = append(pkgWrappers, wrapper)
			}
			if skipReason != "" {
				pkgSkipped++
			}
		}

		if len(pkgWrappers) > 0 {
			b.WriteString(fmt.Sprintf(";;;;; Package: %s (%d wrappers", info.ImportPath, len(pkgWrappers)))
			if pkgSkipped > 0 {
				b.WriteString(fmt.Sprintf(", %d skipped", pkgSkipped))
			}
			b.WriteString(")\n")
			for _, w := range pkgWrappers {
				b.WriteString(w)
				b.WriteString("\n")
			}
			b.WriteString("\n")
		}

		totalWrappers += len(pkgWrappers)
		totalSkipped += pkgSkipped
	}

	log.Printf("Generated %d Lisp wrappers (%d skipped)", totalWrappers, totalSkipped)

	outPath := "go_auto_wrapper.lisp"
	return os.WriteFile(outPath, []byte(b.String()), 0644)
}


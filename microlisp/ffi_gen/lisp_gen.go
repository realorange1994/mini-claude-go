// generateWrapperForFn creates a Lisp (define) wrapper for a Go function.
func generateWrapperForFn(importPath, pkgName string, sig FuncSig) (string, string) {
	fullName := importPath + "." + sig.Name

	if skipFuncs[fullName] {
		return "", "explicitly skipped"
	}

	for _, pt := range sig.ParamTypes {
		for _, pattern := range incompatibleParamPatterns {
			if strings.Contains(pt, pattern) {
				return "", "incompatible param: " + pt
			}
		}
	}

	if len(sig.ReturnType) == 1 && sig.ReturnType[0] == "error" {
		return "", "returns only error"
	}

	if sig.Variadic && len(sig.ParamTypes) > 0 {
		lastParam := sig.ParamTypes[len(sig.ParamTypes)-1]
		for _, pattern := range incompatibleParamPatterns {
			if strings.Contains(lastParam, pattern) {
				return "", "variadic with incompatible: " + lastParam
			}
		}
	}

	lispName := lispFuncName(importPath, sig.Name)
	params := lispParamNames(sig.ParamTypes, sig.Variadic)

	if len(params) == 0 {
		return fmt.Sprintf("(define (%s) (funcall (go:import \"%s.%s\")))",
			lispName, importPath, sig.Name), ""
	}

	return fmt.Sprintf("(define (%s %s) (funcall (go:import \"%s.%s\") %s))",
		lispName, params, importPath, sig.Name, params), ""
}

// lispFuncName converts a Go function name to a Lisp-style name.
func lispFuncName(importPath, goName string) string {
	prefix, ok := lispPkgPrefix[importPath]
	if !ok {
		parts := strings.Split(importPath, "/")
		prefix = parts[len(parts)-1] + "-"
	}
	name := camelToKebab(goName)
	return prefix + name
}

// camelToKebab converts CamelCase to kebab-case.
func camelToKebab(s string) string {
	var result []byte
	for i := 0; i < len(s); i++ {
		c := s[i]
		if c >= 'A' && c <= 'Z' {
			if i > 0 {
				prev := s[i-1]
				if prev >= 'a' && prev <= 'z' {
					result = append(result, '-')
				} else if i+1 < len(s) && s[i+1] >= 'a' && s[i+1] <= 'z' {
					result = append(result, '-')
				}
			}
			result = append(result, c+32)
		} else {
			result = append(result, c)
		}
	}
	return string(result)
}

// lispParamNames generates Lisp parameter names from Go parameter types.
func lispParamNames(paramTypes []string, variadic bool) string {
	if len(paramTypes) == 0 {
		return ""
	}

	names := make([]string, len(paramTypes))
	used := map[string]int{}

	for i, pt := range paramTypes {
		base := typeToParamName(pt)
		if n, ok := used[base]; ok {
			used[base] = n + 1
			names[i] = fmt.Sprintf("%s%d", base, n+1)
		} else {
			used[base] = 1
			names[i] = base
		}
	}

	if variadic && len(names) > 0 {
		names[len(names)-1] = "." + names[len(names)-1]
	}

	return strings.Join(names, " ")
}

// typeToParamName maps Go type strings to short Lisp parameter names.
func typeToParamName(goType string) string {
	if idx := strings.LastIndex(goType, "."); idx >= 0 {
		goType = goType[idx+1:]
	}
	switch {
	case goType == "string":
		return "s"
	case goType == "int" || goType == "int64" || goType == "int32":
		return "n"
	case goType == "float64" || goType == "float32":
		return "x"
	case goType == "bool":
		return "b"
	case goType == "error":
		return "err"
	case goType == "byte" || goType == "rune":
		return "c"
	case strings.HasPrefix(goType, "[]"):
		return "lst"
	case strings.HasPrefix(goType, "*"):
		return "ptr"
	case strings.HasSuffix(goType, "Reader"):
		return "r"
	case strings.HasSuffix(goType, "Writer"):
		return "w"
	case strings.HasSuffix(goType, "Closer"):
		return "cl"
	case strings.HasSuffix(goType, "Handler"):
		return "h"
	default:
		if len(goType) > 0 {
			return strings.ToLower(goType[:1])
		}
		return "v"
	}
}

package microlisp

func typeStr(v *Value) string {
	switch v.typ {
	case VNil:
		return "NULL"
	case VNum:
		if v.isFloat {
			return "SINGLE-FLOAT"
		}
		return "INTEGER"
	case VRat:
		return "RATIONAL"
	case VComplex:
		return "COMPLEX"
	case VStr:
		return "STRING"
	case VSym:
		return "SYMBOL"
	case VBool:
		return "BOOLEAN"
	case VPair:
		return "PAIR"
	case VPrim:
		return "PROCEDURE"
	case VFunc:
		return "PROCEDURE"
	case VMacro:
		return "MACRO"
	case VClass:
		return "CLASS"
	case VRestart:
		return "RESTART"
	case VGeneric:
		return "GENERIC"
	case VInstance:
		return "INSTANCE"
	case VVHash:
		return "HASH-TABLE"
	case VThread:
		return "THREAD"
	case VLock:
		return "LOCK"
	case VChar:
		return "CHARACTER"
	case VStream:
		return "STREAM"
	case VArray:
		return "ARRAY"
	case VMultiVal:
		return "MULTI-VALUE"
	case VBigInt:
		return "INTEGER"
	case VPackage:
		return "PACKAGE"
	case VReadtable:
		return "READTABLE"
	case VPathname:
		return "PATHNAME"
	case VRandomState:
		return "RANDOM-STATE"
	case VMethod:
		return "METHOD"
	default:
		return "UNKNOWN"
	}
}

